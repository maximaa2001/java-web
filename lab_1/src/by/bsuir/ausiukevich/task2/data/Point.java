package by.bsuir.ausiukevich.task2.data;

public class Point {
    private Double x;
    private Double y;

    public Point() {
    }

    public Double getX() {
        return x;
    }

    public void setX(Double x) {
        this.x = x;
    }

    public Double getY() {
        return y;
    }

    public void setY(Double y) {
        this.y = y;
    }
}
